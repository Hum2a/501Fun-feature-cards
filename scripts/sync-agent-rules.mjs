// Mirror `.cursor/rules/*.mdc` into `.claude/rules/` and `.agents/rules/`
// so Cursor, Claude Code, and other agents share the same guidance.
//
// Usage:
//   npm run rules:sync          # write mirrored rule files
//   npm run rules:sync:check    # exit 1 if mirrors are stale (CI)

import { mkdirSync, readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = dirname(dirname(fileURLToPath(import.meta.url)));
const SOURCE_DIR = join(ROOT, '.cursor', 'rules');
const TARGET_DIRS = [join(ROOT, '.claude', 'rules'), join(ROOT, '.agents', 'rules')];

const BANNER =
  '<!-- Generated by `npm run rules:sync`. Source of truth: `.cursor/rules/`. Do not edit directly. -->\n\n';

/** @param {string} mdcName */
function toMdName(mdcName) {
  return mdcName.replace(/\.mdc$/i, '.md');
}

/** @param {string} body */
function renderMirror(body) {
  return `${BANNER}${body.endsWith('\n') ? body : `${body}\n`}`;
}

function listSourceRules() {
  return readdirSync(SOURCE_DIR)
    .filter((name) => name.endsWith('.mdc'))
    .sort();
}

function syncRules() {
  const rules = listSourceRules();
  if (rules.length === 0) {
    console.error(`No .mdc files found in ${SOURCE_DIR}`);
    process.exit(1);
  }

  for (const targetDir of TARGET_DIRS) {
    mkdirSync(targetDir, { recursive: true });
  }

  let written = 0;
  for (const file of rules) {
    const body = readFileSync(join(SOURCE_DIR, file), 'utf8');
    const mirror = renderMirror(body);
    const outName = toMdName(file);
    for (const targetDir of TARGET_DIRS) {
      writeFileSync(join(targetDir, outName), mirror, 'utf8');
      written += 1;
    }
  }

  console.log(
    `Synced ${rules.length} rule(s) → .claude/rules/ and .agents/rules/ (${written} files).`,
  );
}

function checkRules() {
  const rules = listSourceRules();
  const stale = [];

  for (const file of rules) {
    const expected = renderMirror(readFileSync(join(SOURCE_DIR, file), 'utf8'));
    const outName = toMdName(file);
    for (const targetDir of TARGET_DIRS) {
      const path = join(targetDir, outName);
      let actual;
      try {
        actual = readFileSync(path, 'utf8');
      } catch {
        stale.push(path);
        continue;
      }
      if (actual !== expected) {
        stale.push(path);
      }
    }
  }

  if (stale.length > 0) {
    console.error('Agent rule mirrors are out of date:\n');
    for (const path of stale) {
      console.error(`  ${path}`);
    }
    console.error('\nRun: npm run rules:sync');
    process.exit(1);
  }

  console.log(`Agent rule mirrors are up to date (${rules.length} rule(s)).`);
}

if (process.argv.includes('--check')) {
  checkRules();
} else {
  syncRules();
}
